    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        try {
            Validator.startValidation()
            .validateNotNullOrEmpty(txtStatus.getText(), "Status trebovanja je obavezno polje")
            .throwIfInvalide();
            
            Trebovanje t=new Trebovanje();
            t.setId(Integer.parseInt(txtTrebovanjeID.getText().trim()));
            dcDatumTrebovanja.setDateFormatString("yyyy-MM-dd");
            java.util.Date jud=dcDatumTrebovanja.getDate();
            java.sql.Date jsd=new java.sql.Date(jud.getTime());
            t.setDatumTrebovanja(jsd);
            t.setStatus(txtStatus.getText().trim());
            t.setPrijemnica((Prijemnica)cmbPrijemnica.getSelectedItem());
            t.setTipTrebovanja((TipTrebovanja) cmbTipTrebovanja.getSelectedItem());
            t.setGotovProizvod((GotovProizvod) cmbGotovProizvod.getSelectedItem());
            t.setMagacin((Magacin)cmbMagacin.getSelectedItem());
            t.setMestoTroska((Magacin)cmbMestoTroska.getSelectedItem());

            Controller.getInstance().editTrebovanje(t);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio trebovanje.", "Uspešno čuvanje trebovanja", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Sistem ne može da zapamti trebovanje", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed


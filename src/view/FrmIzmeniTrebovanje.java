    void setTrebovanje(Trebovanje t) {
        txtTrebovanjeID.setText(String.valueOf(t.getId()));
        dcDatumTrebovanja.setDateFormatString("yyyy-MM-dd");
        dcDatumTrebovanja.setDate(t.getDatumTrebovanja());
        txtStatus.setText(String.valueOf(t.getStatus()));
        cmbPrijemnica.setSelectedItem(t.getPrijemnica());
        cmbTipTrebovanja.setSelectedItem(t.getTipTrebovanja());
        cmbGotovProizvod.setSelectedItem(t.getGotovProizvod());
        cmbMagacin.setSelectedItem(t.getMagacin());
        cmbMestoTroska.setSelectedItem(t.getMestoTroska());
    }
    
    private void prepareView() throws Exception {
       pripremiComboPrijemnica();
       pripremiComboTipTrebovanja();
       pripremiComboGotovProizvod();
       pripremiComboMagacin();
       pripremiComboMestoTroska();
    }

    private void pripremiComboPrijemnica() throws Exception {
        List<Prijemnica> lista = Controller.getInstance().getAllPrijemnica();
        cmbPrijemnica.removeAllItems();
        for (Prijemnica p : lista) {
            cmbPrijemnica.addItem(p);
        }
    }
    
    private void pripremiComboTipTrebovanja() throws Exception {
        List<TipTrebovanja> lista = Controller.getInstance().getAllTipTrebovanja();
        cmbTipTrebovanja.removeAllItems();
        for (TipTrebovanja tt : lista) {
            cmbTipTrebovanja.addItem(tt);
        }
    }
    
    private void pripremiComboGotovProizvod() throws Exception {
        List<GotovProizvod> lista = Controller.getInstance().getAllGotovProizvod();
        cmbGotovProizvod.removeAllItems();
        for (GotovProizvod gp : lista) {
            cmbGotovProizvod.addItem(gp);
        }
    }
    
    private void pripremiComboMagacin() throws Exception {
        List<Magacin> lista = Controller.getInstance().getAllMagacin();
        cmbMagacin.removeAllItems();
        for (Magacin m : lista) {
            cmbMagacin.addItem(m);
        }
    }
    
    private void pripremiComboMestoTroska() throws Exception {
        List<Magacin> lista = Controller.getInstance().getAllMagacin();
        cmbMestoTroska.removeAllItems();
        for (Magacin m : lista) {
            cmbMestoTroska.addItem(m);
        }
    }

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            Validator.startValidation()
            .validateNotNullOrEmpty(txtUkupnaCena.getText(), "Ukupna cena je obavezno polje")
            .throwIfInvalide();
            
            Racun r = new Racun();
            dcDatumIzdavanja.setDateFormatString("yyyy-MM-dd");
            java.util.Date jud=dcDatumIzdavanja.getDate();
            java.sql.Date jsd=new java.sql.Date(jud.getTime());
            r.setDatumIzdavanja(jsd);
            dcDatumPrometa.setDateFormatString("yyyy-MM-dd");
            java.util.Date jud1=dcDatumPrometa.getDate();
            java.sql.Date jsd1=new java.sql.Date(jud1.getTime());
            r.setDatumPrometa(jsd1);
            r.setTrebovanje((Trebovanje)cmbTrebovanje.getSelectedItem());
            r.setValuta((Valuta)cmbValuta.getSelectedItem());
            r.setPartner((Partner)cmbPartner.getSelectedItem());
            r.setRegionIzdavanja((Region) cmbRegionIzdavanja.getSelectedItem());
            r.setGradIzdavanja((Grad) cmbGradIzdavanja.getSelectedItem());
            r.setRegionPrometa((Region)cmbRegionPrometa.getSelectedItem());
            r.setGradPrometa((Grad)cmbGradPrometa.getSelectedItem());
            r.setPrijemnica((Prijemnica) cmbPrijemnica.getSelectedItem());
            r.setUkupnaCena(Double.parseDouble(txtUkupnaCena.getText().trim()));

            Controller.getInstance().addRacun(r);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio račun.", "Uspešno čuvanje računa", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Sistem ne može da izmeni redundantne atribute (prijemnicaID i ukupna cena).", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Sistem ne može da zapamti račun", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDodajActionPerformed


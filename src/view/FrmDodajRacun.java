    private void prepareView() throws Exception {
       pripremiComboTrebovanje();
       pripremiComboValuta();
       pripremiComboPartner();
       pripremiComboRegionIzdavanja();
       pripremiComboGradIzdavanja();
       pripremiComboRegionPrometa();
       pripremiComboGradPrometa();
       pripremiComboPrijemnica();
    }

    private void pripremiComboTrebovanje() throws Exception {
        List<Trebovanje> lista = Controller.getInstance().getAllTrebovanje();
        cmbTrebovanje.removeAllItems();
        for (Trebovanje t : lista) {
            cmbTrebovanje.addItem(t);
        }
    }
    

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        try {
            Validator.startValidation()
            .validateNotNullOrEmpty(txtImePrezime.getText(), "Ime i prezime je obavezno polje")
            .validateNotNullOrEmpty(txtBrojTelefona.getText(), "Broj telefona je obavezno polje")
            .throwIfInvalide();
            
            Kupac k=new Kupac();
            k.setId(Integer.parseInt(txtKupacID.getText().trim()));
            k.setImePrezime(txtImePrezime.getText().trim());
            k.setBrojTelefona(txtBrojTelefona.getText().trim());
            k.setRegion((Region) cmbRegion.getSelectedItem());
            k.setGrad((Grad) cmbGrad.getSelectedItem());
            k.setUlica((Ulica)cmbUlica.getSelectedItem());
            k.setBroj((Broj)cmbBroj.getSelectedItem());

            Controller.getInstance().editKupac(k);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio kupca.", "Uspe≈°no ƒçuvanje kupca", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Sistem ne mo≈æe da zapamti kupca", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed


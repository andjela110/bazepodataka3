    void setKupac(Kupac k) {
        txtKupacID.setText(String.valueOf(k.getId()));
        txtImePrezime.setText(String.valueOf(k.getImePrezime()));
        txtBrojTelefona.setText(String.valueOf(k.getBrojTelefona()));
        cmbRegion.setSelectedItem(k.getRegion());
        cmbGrad.setSelectedItem(k.getGrad());
        cmbUlica.setSelectedItem(k.getUlica());
        cmbBroj.setSelectedItem(k.getBroj());
    }

    private void prepareView() throws Exception {
       pripremiComboRegion();
       pripremiComboGrad();
       pripremiComboUlica();
       pripremiComboBroj();
    }

    private void pripremiComboRegion() throws Exception {
        List<Region> listar = Controller.getInstance().getAllRegion();
        cmbRegion.removeAllItems();
        for (Region r : listar) {
            cmbRegion.addItem(r);
        }
    }
    private void pripremiComboGrad() throws Exception {
        List<Grad> listag = Controller.getInstance().getAllGrad();
        cmbGrad.removeAllItems();
        for (Grad g : listag) {
            cmbGrad.addItem(g);
        }
    }

    private void pripremiComboUlica() throws Exception {
        List<Ulica> listau = Controller.getInstance().getAllUlica();
        cmbUlica.removeAllItems();
        for (Ulica u : listau) {
            cmbUlica.addItem(u);
        }
    }
    private void pripremiComboBroj() throws Exception {
        List<Broj> listab = Controller.getInstance().getAllBroj();
        cmbBroj.removeAllItems();
        for (Broj b : listab) {
            cmbBroj.addItem(b);
        }
    }


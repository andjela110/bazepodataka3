    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        try {
            Validator.startValidation()
            .validateNotNullOrEmpty(txtStatus.getText(), "Status prijemnice je obavezno polje")
            .throwIfInvalide();
            
            Prijemnica p=new Prijemnica();
            dcDatumOd.setDateFormatString("yyyy-MM-dd");
            java.util.Date jud=dcDatumOd.getDate();
            java.sql.Date jsd=new java.sql.Date(jud.getTime());
            dcDatumDospeca.setDateFormatString("yyyy-MM-dd");
            java.util.Date jud1=dcDatumDospeca.getDate();
            java.sql.Date jsd1=new java.sql.Date(jud1.getTime());
            p.setDatumOd(jsd);
            p.setDatumDospeca(jsd1);
            p.setStatus(txtStatus.getText().trim());
            p.setIzvestajServisera((IzvestajServisera) cmbIzvestajServisera.getSelectedItem());
            p.setNacinPlacanja((NacinPlacanja) cmbNacinPlacanja.getSelectedItem());
            p.setOrganizacionaJedinica((OrganizacionaJedinica)cmbOrganizacionaJedinica.getSelectedItem());
            p.setTipPrijemnice((TipPrijemnice)cmbTipPrijemnice.getSelectedItem());
            p.setMagacin((Magacin)cmbMagacin.getSelectedItem());
            p.setPartner((Partner)cmbPartner.getSelectedItem());
            

            Controller.getInstance().addPrijemnica(p);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio prijemnicu.", "Uspešno čuvanje prijemnice", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Sistem ne može da zapamti prijemnicu", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDodajActionPerformed


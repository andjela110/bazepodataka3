    private void prepareView() throws Exception {
       pripremiComboIzvestajServisera();
       pripremiComboNacinPlacanja();
       pripremiComboOrganizacionaJedinica();
       pripremiComboTipPrijemnice();
       pripremiComboMagacin();
       pripremiComboPartner();
    }

    private void pripremiComboIzvestajServisera() throws Exception {
        List<IzvestajServisera> listai = Controller.getInstance().getAllIzvestajServisera();
        cmbIzvestajServisera.removeAllItems();
        for (IzvestajServisera i : listai) {
            cmbIzvestajServisera.addItem(i);
        }
    }
    
    private void pripremiComboNacinPlacanja() throws Exception {
        List<NacinPlacanja> listan = Controller.getInstance().getAllNacinPlacanja();
        cmbNacinPlacanja.removeAllItems();
        for (NacinPlacanja n : listan) {
            cmbNacinPlacanja.addItem(n);
        }
    }
    private void pripremiComboOrganizacionaJedinica() throws Exception {
        List<OrganizacionaJedinica> listao = Controller.getInstance().getAllOrganizacionaJedinica();
        cmbOrganizacionaJedinica.removeAllItems();
        for (OrganizacionaJedinica o : listao) {
            cmbOrganizacionaJedinica.addItem(o);
        }
    }
    private void pripremiComboTipPrijemnice() throws Exception {
        List<TipPrijemnice> listat = Controller.getInstance().getAllTipPrijemnice();
        cmbTipPrijemnice.removeAllItems();
        for (TipPrijemnice t : listat) {
            cmbTipPrijemnice.addItem(t);
        }
    }

    private void pripremiComboMagacin() throws Exception {
        List<Magacin> listam = Controller.getInstance().getAllMagacin();
        cmbMagacin.removeAllItems();
        for (Magacin m : listam) {
            cmbMagacin.addItem(m);
        }
    }
    private void pripremiComboPartner() throws Exception {
        List<Partner> listap = Controller.getInstance().getAllPartner();
        cmbPartner.removeAllItems();
        for (Partner p : listap) {
            cmbPartner.addItem(p);
        }
    }

